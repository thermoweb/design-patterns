package org.thermoweb.designpatterns.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("turning on popcorn popper");
    }

    public void pop() {
        System.out.println("popping some popcorn");
    }

    public void off() {
        System.out.println("turning off popcorn popper");
    }
}
